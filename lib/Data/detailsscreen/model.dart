import 'package:cleanarchetectureflutterguy/Domain/detailsscreen/entity.dart';

class DetailsModel extends DetailsEntity {
  DetailsModel({
    required id,
    required title,
    required description,
    required category,
    required price,
    required discountPercentage,
    required rating,
    required stock,
    required tags,
    required sku,
    required weight,
    required dimensions,
    required warrantyInformation,
    required shippingInformation,
    required availabilityStatus,
    required reviews,
    required returnPolicy,
    required minimumOrderQuantity,
    required meta,
    required images,
    required thumbnail,
    required brand,
  }) : super(
            id: id,
            title: title,
            description: description,
            category: category,
            price: price,
            discountPercentage: discountPercentage,
            rating: rating,
            stock: stock,
            tags: tags,
            sku: sku,
            weight: weight,
            dimensions: dimensions,
            warrantyInformation: warrantyInformation,
            shippingInformation: shippingInformation,
            availabilityStatus: availabilityStatus,
            reviews: reviews,
            returnPolicy: returnPolicy,
            minimumOrderQuantity: minimumOrderQuantity,
            meta: meta,
            images: images,
            thumbnail: thumbnail,
            brand: brand);

  factory DetailsModel.fromJson(Map<String, dynamic> json) => DetailsModel(
        id: json["id"],
        title: json["title"],
        description: json["description"],
        category: json["category"],
        price: json["price"]?.toDouble(),
        discountPercentage: json["discountPercentage"]?.toDouble(),
        rating: json["rating"]?.toDouble(),
        stock: json["stock"],
        tags: List<String>.from(json["tags"].map((x) => x)),
        brand: json.containsKey("brand") ? json["brand"] : "Unknown",
        sku: json["sku"],
        weight: json["weight"],
        dimensions: Dimensions.fromJson(json["dimensions"]),
        warrantyInformation: json["warrantyInformation"],
        shippingInformation: json["shippingInformation"],
        availabilityStatus: json["availabilityStatus"],
        reviews:
            List<Review>.from(json["reviews"].map((x) => Review.fromJson(x))),
        returnPolicy: json["returnPolicy"],
        minimumOrderQuantity: json["minimumOrderQuantity"],
        meta: Meta.fromJson(json["meta"]),
        images: List<String>.from(json["images"].map((x) => x)),
        thumbnail: json["thumbnail"],
      );
}
